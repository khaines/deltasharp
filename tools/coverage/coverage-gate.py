#!/usr/bin/env python3
"""Coverage gate for DeltaSharp (FEAT-00.2 / STORY-00.2.3, #106).

Reads the Cobertura reports emitted by `coverlet.collector` (one per test assembly
per target framework), MERGES them by unioning per-(file, line) hits, computes the
overall line-coverage percentage, and fails (exit 1) when it is below the configured
threshold. Prints a per-assembly breakdown and the measured-vs-threshold result.

Why a merge is required
-----------------------
Coverage is collected with Include=[DeltaSharp.*], so every test assembly instruments
*all* DeltaSharp production assemblies it loads. The same production line therefore
appears in several reports with different hit counts (for example DeltaSharp.Core is
well covered by DeltaSharp.Core.Tests but barely touched by DeltaSharp.Executor.Tests,
and DeltaSharp.Core.Tests runs on both net8.0 and net10.0). Summing per-report
lines-covered/lines-valid would double- or quadruple-count those lines and produce a
meaningless number. The correct metric unions coverage: a line is covered if hits > 0
in ANY report, and the denominator is the set of distinct (file, line) pairs. This is
the same merge semantics ReportGenerator uses, implemented here with the Python stdlib
only (no third-party or network dependency, so the gate is deterministic and offline).

Fail-closed on an unexpected report set (provenance)
----------------------------------------------------
Unioning "whatever reports were globbed" is fail-OPEN in BOTH directions. If one test
assembly's report is lost (a crashed/dropped suite) it silently vanishes from the denominator,
which can make coverage RISE and the gate PASS while a whole suite went missing. Symmetrically,
an EXTRA report (a planted/fake assembly, or a trivially-100%-covered helper) added to the set
inflates the aggregate and dilutes a real regression. Both are injection vectors. To close this,
`expectedAssemblies` (coverage-config.json) is treated as the EXACT set of production assemblies:
the gate exits non-zero (fail-closed) if any expected assembly is MISSING (naming it), OR if any
UNEXPECTED assembly is present (naming it), and it computes the percentage over ONLY the expected
set so no out-of-allowlist package can move the number. CI also deletes stale `TestResults` before
collecting so only in-step-generated reports count.

Defense-in-depth: the allowlist itself cannot drift or be emptied (#457)
-----------------------------------------------------------------------
The allowlist above is only trustworthy if it CANNOT silently revert to fail-open. Two residual
holes are closed here:

  * Empty/absent allowlist -> FAIL CLOSED. An earlier revision fell back to fail-OPEN global
    accounting (union every globbed package) when `expectedAssemblies` was empty or missing. That
    made deleting one config line silently disable every provenance guard. The gate now EXITS
    non-zero when the allowlist is empty/absent — an unconfigured allowlist is treated exactly like
    a missing assembly, never as "measure everything".
  * Allowlist-vs-src drift -> FAIL CLOSED. The allowlist is cross-checked against the ACTUAL set of
    `src/` production projects (every `src/**/*.csproj`, keyed by `<AssemblyName>`), which is the same
    ground truth the MSBuild `DeltaSharpIsProductionAssembly` path gate uses in Directory.Build.props.
    If the two disagree — the allowlist was emptied, a NEW src/ assembly was added without being
    allowlisted, or a removed assembly was left stale in the list — the gate fails, naming the delta.
    This gives the allowlist the same "cannot drift" property as the centrally-hoisted analyzers: a
    new production assembly is measured (or the build is red) rather than silently unmeasured.

Residual (documented, not fully closeable): in-list synthetic spoofing
----------------------------------------------------------------------
The gate trusts a report whose `<package name>` matches an allowlisted assembly. A report that
CLAIMS an allowlisted name but carries synthetic (file, line) rows would union its fake hits into
that assembly and inflate coverage. Content alone cannot distinguish a synthetic report from a real
one (both are just Cobertura), and a per-assembly line-count bound would be brittle (it drifts as the
code grows) and trivially evaded by an attacker who pads to the real baseline. The PRIMARY control is
therefore the CI `rm -rf TestResults` in-run wipe: only reports generated by the in-run `dotnet test`
collect step are trusted, so a report planted in the repo or left stale is deleted before collection.
Planting a report mid-run requires arbitrary code execution inside the CI job, which would subvert
the gate (and config) itself and is out of scope for a coverage gate.

Usage
-----
    python3 tools/coverage/coverage-gate.py --results-dir TestResults \
        [--config tools/coverage/coverage-config.json] \
        [--src-root src] [--threshold 87.0] [--summary-out TestResults/coverage-summary.md]

Exit codes: 0 = at/above threshold, 1 = below threshold, 2 = usage/data error
(unreadable/malformed/empty reports; an empty/absent or src-drifted expectedAssemblies allowlist;
or a missing expected / unexpected extra production assembly in the merged report set).
"""

from __future__ import annotations

import argparse
import glob
import json
import os
import re
import sys
import xml.etree.ElementTree as ET
from collections import defaultdict


def _log(msg: str = "") -> None:
    print(msg, flush=True)


def _gha() -> bool:
    return os.environ.get("GITHUB_ACTIONS", "").lower() == "true"


# Repo root relative to this script (tools/coverage/coverage-gate.py -> repo root), so the src/ drift
# check works regardless of the caller's CWD. Overridable via --src-root (used by the self-test).
_REPO_ROOT = os.path.dirname(os.path.dirname(os.path.dirname(os.path.abspath(__file__))))
_DEFAULT_SRC_ROOT = os.path.join(_REPO_ROOT, "src")

_ASSEMBLY_NAME_RE = re.compile(r"<AssemblyName>\s*([^<\s]+)\s*</AssemblyName>")


def _assembly_name(csproj_path: str) -> str:
    """The assembly name a project produces: its <AssemblyName>, else the .csproj file stem.

    By repo convention the two are equal, but reading <AssemblyName> when present keeps the drift
    check correct even if a project ever renames its output.
    """
    try:
        with open(csproj_path, "r", encoding="utf-8") as handle:
            match = _ASSEMBLY_NAME_RE.search(handle.read())
    except OSError:
        match = None
    if match:
        return match.group(1)
    return os.path.splitext(os.path.basename(csproj_path))[0]


def discover_src_assemblies(src_root: str) -> set[str]:
    """The set of production assembly names under `src_root` — the drift ground truth.

    Every `*.csproj` under src/ is a production assembly (repo layout: src/ holds only shippable
    framework projects; tests live in tests/, examples in samples/). This mirrors the MSBuild
    `DeltaSharpIsProductionAssembly` path gate in Directory.Build.props, so the Python gate and the
    build classify the SAME set and neither can drift from the other. Returns an empty set when
    `src_root` is absent or contains no project (the caller fails closed on that).
    """
    if not os.path.isdir(src_root):
        return set()
    pattern = os.path.join(src_root, "**", "*.csproj")
    return {_assembly_name(path) for path in glob.glob(pattern, recursive=True)}


def find_reports(results_dir: str) -> list[str]:
    """All coverage.cobertura.xml files under results_dir (recursive)."""
    pattern = os.path.join(results_dir, "**", "coverage.cobertura.xml")
    return sorted(glob.glob(pattern, recursive=True))


def merge_reports(report_paths):
    """Union per-(package, file, line) hit state across every report.

    Returns (per_package, covered_lines, total_lines) where per_package maps an
    assembly name to (covered, total). A line counts as covered when hits > 0 in
    ANY report; the denominator is the count of distinct (file, line) pairs.
    """
    # (package, filename, line_number) -> covered? (True once any report hits it)
    line_covered: dict[tuple[str, str, str], bool] = defaultdict(bool)

    for path in report_paths:
        try:
            root = ET.parse(path).getroot()
        except ET.ParseError as exc:
            _log(f"::error::failed to parse coverage report {path}: {exc}")
            raise SystemExit(2)
        for package in root.iter("package"):
            pkg_name = package.get("name", "(unknown)")
            for cls in package.iter("class"):
                filename = cls.get("filename", "")
                lines = cls.find("lines")
                if lines is None:
                    continue
                for line in lines.findall("line"):
                    number = line.get("number", "")
                    try:
                        hits = int(line.get("hits", "0"))
                    except ValueError:
                        hits = 0
                    key = (pkg_name, filename, number)
                    if hits > 0:
                        line_covered[key] = True
                    elif key not in line_covered:
                        line_covered[key] = False

    per_package_total: dict[str, int] = defaultdict(int)
    per_package_covered: dict[str, int] = defaultdict(int)
    for (pkg_name, _filename, _number), covered in line_covered.items():
        per_package_total[pkg_name] += 1
        if covered:
            per_package_covered[pkg_name] += 1

    total = len(line_covered)
    covered = sum(1 for c in line_covered.values() if c)
    per_package = {
        name: (per_package_covered[name], per_package_total[name])
        for name in sorted(per_package_total)
    }
    return per_package, covered, total


def pct(covered: int, total: int) -> float:
    return 100.0 * covered / total if total else 0.0


def render_summary(per_package, covered, total, threshold, measured, passed) -> str:
    lines = []
    lines.append("# Coverage summary")
    lines.append("")
    lines.append(
        "Merged (TFM-deduplicated) line coverage across all Cobertura reports. "
        "A line is covered if hit by any test assembly on any target framework."
    )
    lines.append("")
    lines.append("| Assembly | Covered | Lines | Line % |")
    lines.append("| --- | ---: | ---: | ---: |")
    for name, (cov, tot) in per_package.items():
        lines.append(f"| {name} | {cov} | {tot} | {pct(cov, tot):.2f}% |")
    lines.append(f"| **TOTAL** | **{covered}** | **{total}** | **{measured:.2f}%** |")
    lines.append("")
    lines.append(f"- **Threshold (minimum line coverage):** {threshold:.2f}%")
    lines.append(f"- **Measured line coverage:** {measured:.2f}%")
    lines.append(f"- **Result:** {'PASS' if passed else 'FAIL'}")
    lines.append("")
    return "\n".join(lines)


def load_config(config_path: str) -> tuple[float, float, list[str]]:
    """Read the enforced threshold, ratchet slack, and expected assemblies.

    `expectedAssemblies` is the provenance allowlist: the production assemblies that MUST
    appear in the merged report set. It makes the gate fail-closed on a partial report set.
    """
    with open(config_path, "r", encoding="utf-8") as handle:
        config = json.load(handle)
    threshold = float(config["minimumLineCoverage"])
    slack = float(config.get("ratchetSuggestSlack", 0.0))
    expected = [str(name) for name in config.get("expectedAssemblies", [])]
    return threshold, slack, expected


def main(argv=None) -> int:
    parser = argparse.ArgumentParser(description="DeltaSharp coverage gate")
    parser.add_argument("--results-dir", default="TestResults")
    parser.add_argument(
        "--config",
        default=os.path.join("tools", "coverage", "coverage-config.json"),
        help="JSON config holding minimumLineCoverage (single source of truth).",
    )
    parser.add_argument(
        "--src-root",
        default=_DEFAULT_SRC_ROOT,
        help=(
            "Directory holding the src/ production projects. expectedAssemblies MUST equal the "
            "assemblies discovered here (the fail-closed drift guard); defaults to the repo's src/ "
            "relative to this script. Overridable for the gate self-test."
        ),
    )
    parser.add_argument(
        "--threshold",
        type=float,
        default=None,
        help="Override the configured threshold (for local dry runs only).",
    )
    parser.add_argument("--summary-out", default=None)
    args = parser.parse_args(argv)

    # Always read the config for the provenance allowlist (expectedAssemblies); a
    # --threshold override only substitutes the numeric floor for local dry runs.
    try:
        threshold, slack, expected_assemblies = load_config(args.config)
    except (OSError, ValueError, KeyError) as exc:
        _log(f"::error::could not read coverage config {args.config}: {exc}")
        return 2
    if args.threshold is not None:
        threshold, slack = args.threshold, 0.0

    reports = find_reports(args.results_dir)
    if not reports:
        _log(f"::error::no coverage.cobertura.xml found under {args.results_dir!r}")
        return 2

    per_package, _global_covered, _global_total = merge_reports(reports)
    if not per_package:
        _log("::error::coverage reports contained no measurable production lines")
        return 2

    expected_set = set(expected_assemblies)

    # Fail-closed on an EMPTY/ABSENT allowlist (#457). An unconfigured allowlist must never revert
    # to fail-OPEN global accounting (measure whatever was globbed) — that would let deleting one
    # config line silently disable every provenance guard below. Treat it exactly like a missing
    # assembly: exit non-zero.
    if not expected_set:
        _log(
            "::error::expectedAssemblies is empty or absent in the coverage config — the provenance "
            "allowlist must list the exact set of production assemblies. An empty allowlist is "
            "fail-closed (it must not revert to measuring every globbed package)."
        )
        return 2

    # Fail-closed on allowlist-vs-src DRIFT (#457). The allowlist is only trustworthy if it cannot
    # silently diverge from reality, so cross-check it against the actual src/ production projects
    # (the same ground truth the MSBuild DeltaSharpIsProductionAssembly path gate uses). This catches
    # BOTH an emptied/short allowlist AND a new src/ assembly added without being allowlisted (or a
    # removed one left stale) — the "cannot drift" property the centrally-hoisted analyzers have.
    src_assemblies = discover_src_assemblies(args.src_root)
    if not src_assemblies:
        _log(
            f"::error::could not discover any src/ production project under {args.src_root!r} — the "
            f"allowlist drift check cannot establish its ground truth, so the gate fails closed. "
            f"Pass --src-root pointing at the repository's src/ directory."
        )
        return 2
    if expected_set != src_assemblies:
        missing_from_allowlist = sorted(src_assemblies - expected_set)
        stale_in_allowlist = sorted(expected_set - src_assemblies)
        _log(
            "::error::expectedAssemblies has drifted from the src/ production projects. "
            f"In src/ but NOT allowlisted: {missing_from_allowlist or '(none)'}; "
            f"allowlisted but NOT in src/: {stale_in_allowlist or '(none)'}. Update "
            "expectedAssemblies in coverage-config.json to the exact set of src/ assemblies "
            "(a new production assembly is a deliberate governance event, like the PublicAPI baseline)."
        )
        return 2

    # Fail-closed provenance (symmetric): treat `expectedAssemblies` as the EXACT production set.
    # (1) Every expected assembly MUST be present with measurable lines — a missing (lost/crashed/
    #     dropped) suite must FAIL, never silently shrink the denominator and let coverage rise.
    missing = [
        name
        for name in expected_assemblies
        if per_package.get(name, (0, 0))[1] == 0
    ]
    if missing:
        for name in missing:
            _log(
                f"::error::coverage report is missing expected production assembly "
                f"{name!r} — a lost/dropped suite must fail the gate (fail-closed), not "
                f"vanish from the denominator. Present: {sorted(per_package) or '(none)'}"
            )
        return 2

    # (2) No UNEXPECTED assembly may be present — an extra/planted/trivially-covered package would
    #     inflate the aggregate and dilute a real regression. A genuinely new src/ production
    #     assembly is a deliberate governance event: add it to `expectedAssemblies` (mirrors the
    #     PublicAPI baseline). `expected_set` is guaranteed non-empty and src-matched by the guards
    #     above, so this is always enforced.
    unexpected = sorted(name for name in per_package if name not in expected_set)
    if unexpected:
        _log(
            f"::error::coverage report contains unexpected production assembl"
            f"{'y' if len(unexpected) == 1 else 'ies'} not in the expectedAssemblies "
            f"allowlist: {unexpected}. An out-of-allowlist assembly must not dilute or "
            f"inflate the aggregate — add a genuine new src/ production assembly to the "
            f"allowlist deliberately, or remove the planted/leaked report."
        )
        return 2

    # Compute coverage over EXACTLY the governed (allowlisted) set so no out-of-allowlist package can
    # move the number. The allowlist is guaranteed non-empty and equal to the src/ production set by
    # the empty-allowlist and drift guards above, so there is no fail-open "measure everything"
    # fallback — an unconfigured allowlist has already failed the gate closed.
    accounted = {name: value for name, value in per_package.items() if name in expected_set}
    covered = sum(cov for (cov, _tot) in accounted.values())
    total = sum(tot for (_cov, tot) in accounted.values())
    if total == 0:
        _log("::error::coverage reports contained no measurable production lines")
        return 2

    raw_measured = pct(covered, total)
    measured = round(raw_measured, 2)  # display only; the pass decision uses the unrounded value

    _log(f"Coverage gate — merged {len(reports)} Cobertura report(s), TFM-deduplicated")
    _log("")
    name_w = max((len(n) for n in per_package), default=10)
    header = f"  {'Assembly'.ljust(name_w)}   Covered    Lines    Line%"
    _log(header)
    _log("  " + "-" * (len(header) - 2))
    for name, (cov, tot) in per_package.items():
        _log(f"  {name.ljust(name_w)}   {cov:>7}  {tot:>7}   {pct(cov, tot):6.2f}%")
    _log("  " + "-" * (len(header) - 2))
    _log(f"  {'TOTAL'.ljust(name_w)}   {covered:>7}  {total:>7}   {measured:6.2f}%")
    _log("")

    passed = raw_measured >= threshold
    detail = f"measured line coverage {measured:.2f}% vs threshold {threshold:.2f}%"
    if passed:
        _log(f"PASS: {detail}")
        if _gha():
            _log(f"::notice::coverage gate PASS — {detail}")
        headroom = raw_measured - threshold
        if slack > 0 and headroom >= slack:
            hint = (
                f"coverage has {headroom:.2f}% headroom over the floor; ratchet "
                f"minimumLineCoverage up toward {measured:.2f}% (policy: threshold only rises)"
            )
            _log(f"RATCHET SUGGESTION: {hint}")
            if _gha():
                _log(f"::notice::{hint}")
    else:
        _log(f"FAIL: {detail}")
        if _gha():
            _log(f"::error::coverage gate FAIL — {detail}")

    if args.summary_out:
        summary = render_summary(per_package, covered, total, threshold, measured, passed)
        os.makedirs(os.path.dirname(os.path.abspath(args.summary_out)), exist_ok=True)
        with open(args.summary_out, "w", encoding="utf-8") as handle:
            handle.write(summary)
        _log(f"Wrote summary to {args.summary_out}")
        step_summary = os.environ.get("GITHUB_STEP_SUMMARY")
        if step_summary:
            with open(step_summary, "a", encoding="utf-8") as handle:
                handle.write(summary + "\n")

    return 0 if passed else 1


if __name__ == "__main__":
    sys.exit(main())
